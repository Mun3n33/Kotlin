fun main() {
    //Arithmetic operators
    var x = 90
    var y = 5
    println(x + y)
    println(x * y)
    println(x - y)
    println(x / y)
    println(x % y)//Modulus

    //Comparison operators
    var num1 = 45
    var num2 = 30
    println(num1 < num2)
    println(num1 > num2)
    println(num1 >= num2)
    println(num1 <= num2)
    println(num1 == num2)
    println(num1 != num2)


    //Assignments operators
    var number = 10
    println(number)

    number += 2
    println(number)

    //Logic operators - &&,||,!
    println( (100 > 20) && (80 <10) )//and
    println( (100 > 20) || (80 <10) )//or
    println( !(100 > 20) || (80 <10) )//not


}